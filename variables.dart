void main() {
  // integer data type
  int age = 30;
  print('Age: $age');

  //  double data type
  double height = 6.2;
  print('Height: $height');

  // string data type
  String name = 'Keya Junior';
  print('Name: $name');

  // list data type
  List<String> fruits = ['Mango', 'Pineapple', 'Guava'];
  print('Fruits: $fruits');

  // map data type
  Map<String, String> person = {
    'name': 'David',
    'age': '17',
    'city': 'Eldoret'
  };
  print('Person: $person');
}
